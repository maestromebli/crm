    "ACCEPTED_BY_CHIEF",
    "CONSTRUCTOR_ASSIGNED",
    "CONSTRUCTOR_IN_PROGRESS",
    "FILES_PACKAGE_UPLOADED",
    "FILES_VALIDATED",
    "APPROVED_BY_CHIEF",
    "TASKS_DISTRIBUTED",
