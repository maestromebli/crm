  const queue = DEMO_QUEUE.find((item) => item.id === flowId);
  if (!queue) return null;
  const baseStepStates = {
    ACCEPTED_BY_CHIEF: "LOCKED",
    CONSTRUCTOR_ASSIGNED: "LOCKED",
    CONSTRUCTOR_IN_PROGRESS: "LOCKED",
    FILES_PACKAGE_UPLOADED: "LOCKED",
    FILES_VALIDATED: "LOCKED",
    APPROVED_BY_CHIEF: "LOCKED",
    TASKS_DISTRIBUTED: "LOCKED",
  } as const;
