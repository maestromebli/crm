/** Текстові стандарти для міні-штабів дільниць (операційні правила на екрані). */
export type MiniHqStandardBlock = {
  headline: string;
  intro: string;
  bullets: string[];
};

/** Заголовок блоку чеклісту на картці залежно від дільниці */
export function workshopMaterialsPanelTitle(stageKey: WorkshopKanbanStageKey): string {
  switch (stageKey) {
    case "CUTTING":
      return "Плитні матеріали та наявність";
    case "EDGING":
      return "Деталі замовлення та кромка";
    case "DRILLING":
      return "Готовність до присадки";
    case "ASSEMBLY":
      return "Комплектація збірки";
    case "PAINTING":
      return "Матеріали фарбування";
    case "PACKAGING":
      return "Пакування";
    default:
      return "Матеріали";
  }
}

